package com.example.prueba1.Models;

public class Model {

 public String name;

 public String img;

 public String level;

 public Model(String name, String img, String level){
  this.name=name;
  this.img=img;
  this.level=level;
 }
 public String getName() {
  return name;
 }

 public String getImg() {
  return img;
 }

 public String getLevel() {
  return level;
 }


}
